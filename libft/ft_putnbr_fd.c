#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	int		tmp;
	char	c;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n *= -1;
		}
		if (n >= 10)
		{
			tmp = n % 10;
			n /= 10;
			ft_putnbr_fd(n, fd);
		}
		else
			tmp = n;
		c = tmp + '0';
		write(fd, &c, 1);
	}
}
