#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	i;
	size_t	j;
	size_t	sizestr;
	char	*str;

	if (!s1 || !s2)
		return (NULL);
	sizestr = ft_strlen(s1) + ft_strlen(s2);
	str = malloc(sizeof(char) * (sizestr + 1));
	if (str == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	j = 0;
	while (s2[j])
	{
		str[i + j] = s2[j];
		j++;
	}
	str[i + j] = '\0';
	return (str);
}
